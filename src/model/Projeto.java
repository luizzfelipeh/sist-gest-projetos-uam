package model;

/**
 *
 * @author luizf
 */
public class Projeto {

    private String nome;
    private String descricao;
    private String dataInicio;
    private String dataFim;
    private String status;
    private String gerenteResponsavel;

    // Construtor
    public Projeto(String nome, String descricao, String dataInicio, String dataFim, String status, String gerenteResponsavel) {
        this.nome = nome;
        this.descricao = descricao;
        this.dataInicio = dataInicio;
        this.dataFim = dataFim;
        this.status = status;
        this.gerenteResponsavel = gerenteResponsavel;
    }

    // Getters e Setters
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getDataInicio() {
        return dataInicio;
    }

    public void setDataInicio(String dataInicio) {
        this.dataInicio = dataInicio;
    }

    public String getDataFim() {
        return dataFim;
    }

    public void setDataFim(String dataFim) {
        this.dataFim = dataFim;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getGerenteResponsavel() {
        return gerenteResponsavel;
    }

    public void setGerenteResponsavel(String gerenteResponsavel) {
        this.gerenteResponsavel = gerenteResponsavel;
    }

    // MÃ©todo para salvar em CSV
    @Override
    public String toString() {
        return nome + ";" + descricao + ";" + dataInicio + ";" + dataFim + ";" + status + ";" + gerenteResponsavel;
    }
}


